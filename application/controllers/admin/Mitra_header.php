<?php

if (!defined('BASEPATH'))
    exit('No direct script access allowed');

class Mitra_header extends CI_Controller
{
    function __construct()
    {
        parent::__construct();
        $this->load->model('Mitra_header_model');
        $this->load->library('form_validation');
    }

    public function index()
    {
        $q = urldecode($this->input->get('q', TRUE));
        $start = intval($this->input->get('start'));
        
        if ($q <> '') {
            $config['base_url'] = base_url() . 'mitra_header/index.html?q=' . urlencode($q);
            $config['first_url'] = base_url() . 'mitra_header/index.html?q=' . urlencode($q);
        } else {
            $config['base_url'] = base_url() . 'mitra_header/index.html';
            $config['first_url'] = base_url() . 'mitra_header/index.html';
        }

        $config['per_page'] = 10;
        $config['page_query_string'] = TRUE;
        $config['total_rows'] = $this->Mitra_header_model->total_rows($q);
        $mitra_header = $this->Mitra_header_model->get_limit_data($config['per_page'], $start, $q);

        $this->load->library('pagination');
        $this->pagination->initialize($config);

        $data = array(
            'mitra_header_data' => $mitra_header,
            'q' => $q,
            'pagination' => $this->pagination->create_links(),
            'total_rows' => $config['total_rows'],
            'start' => $start,
        );
        $this->load->view('mitra_header/mitra_header_list', $data);
    }

    public function read($id) 
    {
        $row = $this->Mitra_header_model->get_by_id($id);
        if ($row) {
            $data = array(
		'id' => $row->id,
		'background' => $row->background,
	    );
            $this->load->view('mitra_header/mitra_header_read', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('mitra_header'));
        }
    }

    public function create() 
    {
        $data = array(
            'button' => 'Create',
            'action' => site_url('mitra_header/create_action'),
	    'id' => set_value('id'),
	    'background' => set_value('background'),
	);
        $this->load->view('mitra_header/mitra_header_form', $data);
    }
    
    public function create_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->create();
        } else {
            $data = array(
		'background' => $this->input->post('background',TRUE),
	    );

            $this->Mitra_header_model->insert($data);
            $this->session->set_flashdata('message', 'Create Record Success');
            redirect(site_url('mitra_header'));
        }
    }
    
    public function update($id) 
    {
        $row = $this->Mitra_header_model->get_by_id($id);

        if ($row) {
            $data = array(
                'button' => 'Update',
                'action' => site_url('mitra_header/update_action'),
		'id' => set_value('id', $row->id),
		'background' => set_value('background', $row->background),
	    );
            $this->load->view('mitra_header/mitra_header_form', $data);
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('mitra_header'));
        }
    }
    
    public function update_action() 
    {
        $this->_rules();

        if ($this->form_validation->run() == FALSE) {
            $this->update($this->input->post('id', TRUE));
        } else {
            $data = array(
		'background' => $this->input->post('background',TRUE),
	    );

            $this->Mitra_header_model->update($this->input->post('id', TRUE), $data);
            $this->session->set_flashdata('message', 'Update Record Success');
            redirect(site_url('mitra_header'));
        }
    }
    
    public function delete($id) 
    {
        $row = $this->Mitra_header_model->get_by_id($id);

        if ($row) {
            $this->Mitra_header_model->delete($id);
            $this->session->set_flashdata('message', 'Delete Record Success');
            redirect(site_url('mitra_header'));
        } else {
            $this->session->set_flashdata('message', 'Record Not Found');
            redirect(site_url('mitra_header'));
        }
    }

    public function _rules() 
    {
	$this->form_validation->set_rules('background', 'background', 'trim|required');

	$this->form_validation->set_rules('id', 'id', 'trim');
	$this->form_validation->set_error_delimiters('<span class="text-danger">', '</span>');
    }

}

